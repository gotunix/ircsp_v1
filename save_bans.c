int
save_bans ()
{
	Bans	*b;
	FILE	*bandb;

	b = bans;

	if (!(bandb = fopen("bans.db", "w")))
	{
		return 0;
	}
	fprintf(bandb, "; Last Saved %lu\n", time(NULL));
	while (b)
	{
		fprintf(bandb, "%s:%s:%s\n",
			b->chan, b->host, 
			b->setby);
		b = b->next;
	}
	fclose(bandb);
	return 1;
}

